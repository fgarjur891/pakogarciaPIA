# 4. Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

number1 = int(input('Numero1: '))
number2 = int(input('Numero2: '))

sum_ = number1 + number2
subtract = number1 - number2
product_ = number1 * number2
division = number1 / number2

print ('Las operaciones con estos dos números son SUMA ',sum_,'RESTA ', subtract, 'MULTIPLICACION ', product_,'DIVISIÓN ',division)

